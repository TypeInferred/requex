/**
 * This class holds static methods for performing arithmetic.
 */
export default class Calculator {
  /**
   * Adds two numbers together.
   * @param {number} x - The first operand
   * @param {number} y - The second operand
   * @returns {number} - The sum of the first and second operands
   */
  static add(x, y) {
    return x + y;
  }
}